let mode = 'daily';

function getDayOfYear() {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  return Math.floor((now - start) / 86400000);
}

function setMode(m) {
  mode = m;
  document.getElementById('btn-daily').className = 'mode-btn' + (m === 'daily' ? ' active' : '');
  document.getElementById('btn-random').className = 'mode-btn' + (m === 'random' ? ' active' : '');
  document.getElementById('mainBtn').querySelector('span').textContent =
    m === 'daily' ? 'המשפט של היום' : 'משפט אחר';
  showQuote();
}

function showQuote() {
  const el = document.getElementById('quote');
  el.classList.add('fade');
  setTimeout(() => {
    const idx = mode === 'daily'
      ? getDayOfYear() % quotes.length
      : Math.floor(Math.random() * quotes.length);
    el.textContent = quotes[idx];
    document.getElementById('quoteNum').textContent = `משפט ${idx + 1} מתוך ${quotes.length}`;
    el.classList.remove('fade');
  }, 300);
}

function nextQuote() {
  if (mode === 'random') showQuote();
}

function shareWhatsapp() {
  const text = document.getElementById('quote').textContent;
  const msg = encodeURIComponent(`✦ המשפט היומי ✦\n\n"${text}"\n\nhttps://quote-of-the-day-il.netlify.app`);
  window.open(`https://wa.me/?text=${msg}`, '_blank');
}

function copyQuote() {
  const text = document.getElementById('quote').textContent;
  navigator.clipboard.writeText(`"${text}"\n\n— המשפט היומי`).then(() => {
    const btn = document.getElementById('copyBtn');
    const original = btn.innerHTML;
    btn.innerHTML = `<svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><polyline points="20 6 9 17 4 12"/></svg> הועתק!`;
    setTimeout(() => { btn.innerHTML = original; }, 2000);
  });
}

